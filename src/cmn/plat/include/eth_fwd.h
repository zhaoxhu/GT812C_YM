#define ETH_HTTP_PORT  0x50
#define ETH_ARP_PKT_LEN 60
#define IPV4_ADDR_LEN 4
#define ETHER_ADDR_LEN		6

typedef struct {
        epon_macaddr_t          dst;
        epon_macaddr_t          src;
} __attribute__((packed)) epon_macaddr_header_t;

struct ether_header {
    cs_uint8	ether_dhost[ETHER_ADDR_LEN];
    cs_uint8	ether_shost[ETHER_ADDR_LEN];
    cs_uint16	ether_type;
} __attribute__ ((aligned(1), packed));

/*
 * Address Resolution Protocol.
 *
 * See RFC 826 for protocol description.  ARP packets are variable
 * in size; the arphdr structure defines the fixed-length portion.
 * Protocol type values are the same as those for 10 Mb/s Ethernet.
 * It is followed by the variable-sized fields ar_sha, arp_spa,
 * arp_tha and arp_tpa in that order, according to the lengths
 * specified.  Field names used correspond to RFC 826.
 */
struct arphdr {
    cs_uint16 ar_hrd;		/* format of hardware address */
#define ARPHRD_ETHER 	1	/* ethernet hardware format */
#define ARPHRD_IEEE802	6	/* token-ring hardware format */
#define ARPHRD_FRELAY 	15	/* frame relay hardware format */
    cs_uint16 ar_pro;		/* format of protocol address */
    cs_uint8 ar_hln;		/* length of hardware address */
    cs_uint8 ar_pln;		/* length of protocol address */
    cs_uint16 ar_op;		/* one of: */
#define ARPOP_REQUEST	1	/* request to resolve address */
#define ARPOP_REPLY	2	/* response to previous request */
#define ARPOP_REVREQUEST 3	/* request protocol address given hardware */
#define ARPOP_REVREPLY	4	/* response giving protocol address */
#define ARPOP_INVREQUEST 8 	/* request to identify peer */
#define ARPOP_INVREPLY	9	/* response identifying peer */
};

/*
 * Ethernet Address Resolution Protocol.
 *
 * See RFC 826 for protocol description.  Structure below is adapted
 * to resolving internet addresses.  Field names used correspond to
 * RFC 826.
 */
struct ether_arp {
    struct	arphdr ea_hdr;	/* fixed-size header */
    cs_uint8	arp_sha[ETHER_ADDR_LEN];	/* sender hardware address */
    cs_uint8	arp_spa[IPV4_ADDR_LEN];	/* sender protocol address */
    cs_uint8	arp_tha[ETHER_ADDR_LEN];	/* target hardware address */
    cs_uint8	arp_tpa[IPV4_ADDR_LEN];	/* target protocol address */
};
#define	arp_hrd	ea_hdr.ar_hrd
#define	arp_pro	ea_hdr.ar_pro
#define	arp_hln	ea_hdr.ar_hln
#define	arp_pln	ea_hdr.ar_pln
#define	arp_op	ea_hdr.ar_op


